package medrex.commons.vo.census;

import java.io.Serializable;

public class CensusDayReport implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6867979728848187251L;

	private String acount;

	private String dcount;

	private String ocount;

	private String hcount;

	private String acountsf;

	private String dcountsf;

	private String ocountsf;

	private String hcountsf;

	public String getAcount() {
		return acount;
	}

	public void setAcount(String acount) {
		this.acount = acount;
	}

	public String getDcount() {
		return dcount;
	}

	public void setDcount(String dcount) {
		this.dcount = dcount;
	}

	public String getOcount() {
		return ocount;
	}

	public void setOcount(String ocount) {
		this.ocount = ocount;
	}

	public String getHcount() {
		return hcount;
	}

	public void setHcount(String hcount) {
		this.hcount = hcount;
	}

	public String getAcountsf() {
		return acountsf;
	}

	public void setAcountsf(String acountsf) {
		this.acountsf = acountsf;
	}

	public String getDcountsf() {
		return dcountsf;
	}

	public void setDcountsf(String dcountsf) {
		this.dcountsf = dcountsf;
	}

	public String getOcountsf() {
		return ocountsf;
	}

	public void setOcountsf(String ocountsf) {
		this.ocountsf = ocountsf;
	}

	public String getHcountsf() {
		return hcountsf;
	}

	public void setHcountsf(String hcountsf) {
		this.hcountsf = hcountsf;
	}

}
